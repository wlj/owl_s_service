package EDU.pku.sj.rscasd.owls1_1.exception;

public class NotInstanceOfContextException extends Exception {

	private static final long serialVersionUID = -2828466604938782885L;

	public NotInstanceOfContextException() {
		super();
	}

	public NotInstanceOfContextException(String message) {
		super(message);
	}

	public NotInstanceOfContextException(String message, Throwable cause) {
		super(message, cause);
	}

	public NotInstanceOfContextException(Throwable cause) {
		super(cause);
	}
}

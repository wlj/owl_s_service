package edu.pku.util;

public class Most {

	private double min;
	private double max;
	
	public Most() {
		
		this.min = 10000.0;
		this.max = 0.0;
	}
	
	public double getMin() {
		
		return min;
	}
	
	public void setMin(double min) {
		
		this.min = min;
	}
	
	public double getMax() {
		
		return max;
	}
	
	public void setMax(double max) {
		
		this.max = max;
	}
}
